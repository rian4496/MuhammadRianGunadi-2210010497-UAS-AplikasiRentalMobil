    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        Pelanggan pelanggan = getPelangganFromInput();
        if (pelanggan == null) {
            return;
        }

        try {
            pelanggan.save();
            loadPelangganList();
            clearInputFields();
        } catch (SQLException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan pelanggan: " + ex.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        Pelanggan selectedPelanggan = listPelanggan.getSelectedValue();
        if (selectedPelanggan == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih pelanggan yang ingin diubah!", "Validasi Gagal", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        Pelanggan updatedPelanggan = getPelangganFromInput(selectedPelanggan.idPelanggan());
        if (updatedPelanggan == null) {
            return;
        }

        try {
            updatedPelanggan.update();  // Update Pelanggan instead of Mobil
            loadPelangganList();
            clearInputFields();
            javax.swing.JOptionPane.showMessageDialog(this, "Data pelanggan berhasil diperbarui!", "Sukses", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal memperbarui pelanggan: " + ex.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        Pelanggan selectedPelanggan = listPelanggan.getSelectedValue();
        if (selectedPelanggan == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih pelanggan yang ingin dihapus!", "Validasi Gagal", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        int confirm = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Apakah Anda yakin ingin menghapus pelanggan " + selectedPelanggan.nama() + "?",
                "Konfirmasi Hapus",
                javax.swing.JOptionPane.YES_NO_OPTION,
                javax.swing.JOptionPane.WARNING_MESSAGE
        );

        if (confirm != javax.swing.JOptionPane.YES_OPTION) {
            return;
        }

        try {
            selectedPelanggan.delete(); // Delete Pelanggan instead of Mobil
            loadPelangganList();
            clearInputFields();
            javax.swing.JOptionPane.showMessageDialog(this, "Pelanggan berhasil dihapus!", "Sukses", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghapus pelanggan: " + ex.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

