    private void loadPelangganList() {
        loadPelangganList(null);
    }

    private void loadPelangganList(String keyword) {
        try {
            // Get data pelanggan instead of mobil
            List<Pelanggan> pelangganList = Pelanggan.getAll(keyword);

            DefaultListModel<Pelanggan> listModel = new DefaultListModel<>();
            for (Pelanggan pelanggan : pelangganList) {
                listModel.addElement(pelanggan);
            }

            listPelanggan.setModel(listModel);
        } catch (SQLException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal memuat data pelanggan: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

    private Pelanggan getPelangganFromInput() {
        return getPelangganFromInput(null);
    }

    private Pelanggan getPelangganFromInput(Integer id) {
        String nama = txtNamaPengguna.getText().trim();
        if (nama.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nama pelanggan tidak boleh kosong!", "Validasi Gagal", javax.swing.JOptionPane.WARNING_MESSAGE);
            return null;
        }

        String alamat = txtAlamat.getText().trim();
        if (alamat.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Alamat tidak boleh kosong!", "Validasi Gagal", javax.swing.JOptionPane.WARNING_MESSAGE);
            return null;
        }

        String nomorTelepon = txtNomorTelepon.getText().trim();
        if (nomorTelepon.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nomor telepon tidak boleh kosong!", "Validasi Gagal", javax.swing.JOptionPane.WARNING_MESSAGE);
            return null;
        }

        String email = txtEmail.getText().trim();
        if (email.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Email tidak boleh kosong!", "Validasi Gagal", javax.swing.JOptionPane.WARNING_MESSAGE);
            return null;
        }

        // Create Pelanggan from input
        if (id != null) {
            return new Pelanggan(id, nama, alamat, nomorTelepon, email);
        } else {
            return new Pelanggan(nama, alamat, nomorTelepon, email);
        }
    }

    // Phone number verifier (validates only digits and length check)
    class PhoneNumberVerifier extends InputVerifier {

        @Override
        public boolean verify(JComponent input) {
            String telepon = ((javax.swing.JTextField) input).getText().trim();
            if (telepon.isEmpty()) {
                return true;
            }

            // Validating if the phone number starts with "08", "+62" or "62" and has 10-13 digits
            if (telepon.matches("(08\\d{8,11}|\\+62\\d{9,12})")) {
                return true;
            } else {
                javax.swing.JOptionPane.showMessageDialog(input, "Nomor telepon tidak valid!", "Validasi Gagal", javax.swing.JOptionPane.WARNING_MESSAGE);
                ((javax.swing.JTextField) input).setText("");
                return true;
            }

        }
    }

    // Email verifier (validates email format)
    class EmailVerifier extends InputVerifier {

        @Override
        public boolean verify(JComponent input) {
            String email = ((javax.swing.JTextField) input).getText().trim();
            if (email.isEmpty()) {
                return true;
            }

            // Regex for simple email validation
            String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
            if (Pattern.matches(emailRegex, email)) {
                return true;
            } else {
                javax.swing.JOptionPane.showMessageDialog(input, "Email tidak valid!", "Validasi Gagal", javax.swing.JOptionPane.WARNING_MESSAGE);
                ((javax.swing.JTextField) input).setText("");
                return true;
            }
        }
    }

